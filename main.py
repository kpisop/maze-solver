from PIL import Image

# Open the image file
image = Image.open("maze.png").convert("L")  # Convert to grayscale

# Load the pixel data
pixels = image.load()

# Get the size of the image
width, height = image.size

# Define the grid size
grid_size = 40

# Calculate the size of each cell in the grid
cell_width = width // grid_size
cell_height = height // grid_size

# Convert the image to a grid of 0s and 1s
# Assuming white pixels (255) are paths and black pixels (0) are walls
grid = []
for row in range(grid_size):
    grid_row = []
    for col in range(grid_size):
        white_count = 0
        black_count = 0
        for x in range(col * cell_width, (col + 1) * cell_width):
            for y in range(row * cell_height, (row + 1) * cell_height):
                if pixels[x, y] > 128:  # Consider pixels with intensity > 128 as white
                    white_count += 1
                else:
                    black_count += 1
        # Determine if the cell is a path or a wall based on the majority of pixels
        grid_row.append(1 if white_count > black_count else 0)
    grid.append(grid_row)

# Print the grid
for row in grid:
    print(row)

def grid_to_image(grid, cell_width, cell_height, output_path):
    grid_size = len(grid)
    width = grid_size * cell_width
    height = grid_size * cell_height

    # Create a new image with RGB mode
    image = Image.new("RGB", (width, height))

    # Load the pixel data
    pixels = image.load()

    # Convert the grid back to an image
    for row in range(grid_size):
        for col in range(grid_size):
            color = (0, 0, 0)  # Default to black
            if grid[row][col] == 1:
                color = (255, 255, 255)  # White
            elif grid[row][col] == 2:
                color = (255, 255, 0)  # Yellow
            for x in range(col * cell_width, (col + 1) * cell_width):
                for y in range(row * cell_height, (row + 1) * cell_height):
                    pixels[x, y] = color

    # Save the image
    image.save(output_path)

